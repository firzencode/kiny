export const KIN_KNOWLEDGE = `# Kin 语言速参（互动叙事 DSL）

你在为 Kiny 编辑器创作 Kin 互动故事。Kin 是一门以「节点 + 跳转 + 选项」为骨架、逻辑即 JavaScript 的脚本语言。下面是创作所需的核心语法。

## 项目结构
- 项目根放 kiny.json（必需字段 name / version / engine / entry）。引擎自动递归扫描根下所有 .kin，无需 INCLUDE；所有文件共享同一全局节点命名空间。
- entry 指向的入口文件里、第一个节点之前的整段是全局「开场」，故事从这里开始。

## 节点与跳转
- 节点：=== 名字 ===（左右各 3 个等号）。名字可中文、不含空格、全局唯一。执行到底部无跳转会告警——没有隐式 fall-through。
- 子节点：节点内用单个 = 子节点名 切分。同父内 -> 子节点，跨父用 父.子。进入父节点只执行其正文，遇第一个 = 即结束。
- 带参节点：=== 商店(category, discount) ===，参数即局部变量，参数名必须英文。只能经 -> 名字(实参) 进入。
- 跳转：-> 目标 立即跳转（可独立成行或贴文本末尾）。-> END 结束故事，-> DONE 结束当前线程。

## 选项与分支
- 选项：* 一次性（选过消失）/ + 粘性（可重复）。[文本] 内只在列表显示、] 之后只在点击后正文显示；省略 [] 则两处同文。
- 条件选项：{条件} 紧跟 * 之后，假则不显示，如 * {gold >= 5} [买灯笼] -> buy。
- 标签：* (label) [文本] 自动追踪该选项被选次数，{label} 读取（标签名英文、全局唯一）。
- 后备选项：* -> 目标（无文本无条件），其他选项都不可用时触发，每组至多一个。
- 分支体：选中后执行的内容用行首 > 标层级，> 个数 = 嵌套深度；层级减少即汇合，回到 0 整组结束。
- 条件块：@if {cond} / @elif {cond} / @else，分支体与选项共用 > 层级；无结束符，回到选择器层级且非 @elif/@else 时闭合。体内执行 JS 用 > ~。

## 逻辑（就是 JavaScript）
- ~ 起首执行一条 JS 语句：~ let gold = 10 / ~ gold -= 5 / ~ inventory.push("药水")。变量名须 ASCII。文件顶部声明是全局作用域，节点内声明随离开节点销毁。
- 多行 JS 用 ~~~ … ~~~（起止各占一行），块内任意 JS、不能嵌 Kin 语法、不产出文本；只能写在节点正文顶层。
- 插值：文本中 { JS 表达式 } 求值后插入，如 你还剩 {gold} 枚金币。行内条件用三元。undefined/null 输出空串；引用未声明变量编译期报错。
- 函数：用 JS function / 箭头函数写在 ~~~ 块里，全局共享。
- 内置函数（保留标识符，不可作变量名）：random(min,max) 闭区间随机整数、seed_random(n) 设种子、turns()、turns_since("节点名")。

## 文本
- 节点正文里所有非控制行都是普通文本，每行 = 一段输出、行末自动换行。空行被忽略。
- 文本变体（按源码位置自动计数）：seq(...) 依次推进停在最后、cycle(...) 循环、once(...) 用完返回空串、shuffle(...) 随机。
- 内联富文本（明确闭合、可嵌套）：<b> 粗、<i> 斜、<u> 下划线、<s> 删除线、<br> 换行（自闭合）、<color=值>（#rgb/#rrggbb/CSS 具名色）、<size=倍数>（正数倍，相对正文字号）。不接受任意 CSS。仅当 < 后构成合法标签才识别，否则按字面输出。
- 粘连：<> 紧贴文本末尾，取消其后换行让下一段贴上来（即便中间隔着 -> 跳转）。
- 转义：\\{ \\} \\< \\/ \\\\ 任意位置；\\= \\* \\+ \\> \\~ \\@ \\-> 仅行首；\\[ \\] \\( \\) 仅选项行内。
- 注释：// 单行、/* … */ 多行。

## 宿主命令
- @命令(参数) 独占一行、行首顶格，向宿主下达副作用、不产出文本。参数是 JS 表达式，资源用项目根相对路径。
- 内置：@bg_show("assets/x.jpg") / @bg_hide() / @bgm_play("assets/x.mp3") / @bgm_pause() / @bgm_stop()。引擎只认内置命令集，未知命令报错。

## 创作守则
- 写完或改完后，用 validate 命令校验，按诊断修正后再交付。
- 改动落在编辑缓冲（脏标记、可撤销），用户会审阅；需要时可显式 saveFile / saveAll。
- 拿不准某语法的完整规则 / 示例 / 边界时，先用 listKinSpec 看章节目录、再 readKinSpec 取对应章节核对，别凭记忆猜。`
